package courses;

public class RelationalOperators {

	public static void main(String[] args) {
		
		int x = 25 , y = 50;
		//Equal to operator
		System.out.println("is 25 equal 50 ? " + (x==y));
		// Not Equal to operator
		System.out.println("is 25 equal 50 ? " + (x!=y));
				
		//x greater than y operator
		System.out.println("is 25 equal 50 ? " + (x>y));
		//x greater than or equal y  operator
		System.out.println("is 25 equal 50 ? " + (x>=y));
					
		//x less than or equal y  operator
		System.out.println("is 25 equal 50 ? " + (x<=y));
		
		//x less than  y  operator
		System.out.println("is 25 equal 50 ? " + (x<y));
	}

}
